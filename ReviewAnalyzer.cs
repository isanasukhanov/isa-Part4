/* Задача: Анализатор Текстовых Отзывов

Описание:
Создайте программу на C#, которая анализирует отзывы пользователей. Ваша программа должна читать строки текста,
 вводимые пользователем, и определять, является ли отзыв положительным, нейтральным или отрицательным на основе ключевых слов.

Требования:
Чтение и анализ текста: Программа должна считывать текстовые строки (отзывы) и анализировать их. 
Можно использовать простой алгоритм, основанный на наличии определенных слов (например, "хорошо", "отлично" 
для положительных отзывов и "плохо", "ужасно" для отрицательных).

Методы для анализа: Реализуйте методы для определения тональности отзыва. Эти методы должны
 искать ключевые слова и возвращать результат анализа (например, AnalyzeSentiment).

Цикл для обработки отзывов: Используйте цикл для чтения и обработки нескольких отзывов.
 Можно организовать ввод отзывов в консоли до ввода специального символа или строки, 
 которая будет сигнализировать об окончании ввода.

Вывод результатов: После анализа каждого отзыва программа должна выводить его тональность
 (положительный, нейтральный, отрицательный).

Пример работы программы:
Ввод пользователя: "Этот продукт отличный!"
Вывод программы: "Положительный отзыв."в
Ввод пользователя: "Я не доволен этой покупкой."
Вывод программы: "Отрицательный отзыв."

Примеры вводов:
"Прекрасный продукт, очень доволен покупкой!"
"Ужасное обслуживание, никогда больше не вернусь."
"Доставка заняла слишком много времени, но продукт в порядке."
"Отличный сервис и дружелюбный персонал."
"Продукт не соответствует описанию, очень разочарован."
"Средний опыт, ничего особенного."
"Превзошло все мои ожидания – просто великолепно!"
"Качество хорошее, но цена слишком высока."
"Очень удобно и функционально, рекомендую."
"Опыт покупки был неприятным, обслуживание могло быть лучше." */

public static class ReviewAnalyzer
{
    private static bool CheckForPositivity(string review)
    {
        string[] positiveExpressions = { "прекрасн", "доволен", "отличн", "дружел","великолеп"};
        foreach (string expression in  positiveExpressions)
        {
            if (review.ToLower().Contains(expression))
            {
                return true;
            }
        }
        return false;
    }

    private static bool CheckForNegativity(string review)
    {
        string[] negativeExpressions = {"ужасн", "разачаров", "неприятн"};
        foreach (string expression in  negativeExpressions)
        {
            if (review.ToLower().Contains(expression))
            {
                return true;
            }
        }
        return false;
    }

    public static void Start()
    {
       Console.WriteLine("Оставьте отзыв: ");
       string review = Console.ReadLine();
       if (string.IsNullOrWhiteSpace(review))
       {
            Start();
            return;
       }


        bool isPositive = CheckForPositivity(review);
        bool isNegative = CheckForNegativity(review);


        if (isPositive == true)
        {
            Console.WriteLine("Положительный отзыв.");
        }
        else if (isNegative == true)
        {
            Console.WriteLine("Отрицательный отзыв.");
        }
        else 
        {   
            Console.WriteLine("Нейтральный отзыв.");
        }

    }
}